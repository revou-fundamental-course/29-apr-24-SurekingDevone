let countDate = new Date('aug 1, 2021 00:00:00').getTime();

/**
 * Function to count down to a specific date and display the remaining time in days, hours, minutes, and seconds.
 *
 * @param {Date} countDate - The date until which the countdown will run.
 */
function countDown(countDate) {
    let now = new Date().getTime();
    let gap = countDate.getTime() - now;

    let seconds = 1000;
    let minutes = seconds * 60;
    let hours = minutes * 60;
    let days = hours * 24;

    let d = Math.floor(gap / days);
    let h = Math.floor((gap % days) / hours);
    let m = Math.floor((gap % hours) / minutes);
    let s = Math.floor((gap % minutes) / seconds);

    document.getElementById('days').innerText = d;
    document.getElementById('hours').innerText = h;
    document.getElementById('minutes').innerText = m;
    document.getElementById('seconds').innerText = s;
}

setInterval(function(){
    countDown()
},1000);


